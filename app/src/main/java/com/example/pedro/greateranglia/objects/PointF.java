package com.example.pedro.greateranglia.objects;


/**
 *
 * x -> latitude
 * y -> longitude
 *
 */

public class PointF {

    private double x,y;

    public PointF(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }





}
